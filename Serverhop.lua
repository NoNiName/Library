        pcall(function()
            ServerLess()
            if foundAnything ~= "" then
                ServerLess()
            end
        end)
    end
end

function functeleport:Rejoin() 
    game:GetService("TeleportService"):Teleport(game.PlaceId, game:GetService("Players").LocalPlayer)
end

return functeleport
